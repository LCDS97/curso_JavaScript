// Operador Aritiméticos
let salario = 100;

/*
--------------------------------------------------------
  Operadores a serem utilizados para operações
  + = Adição
  - = Subtração
  * = Multiplicação
  / = Divisão
  ** = Exponencial
--------------------------------------------------------
*/

console.log (salario + salario);
console.log (salario - salario);
console.log (salario * salario);
console.log (salario / salario);
console.log (5 ** salario);
console.log("--------------")
/*
--------------------------------------------------------
  Existe os operadores de incremento e decremento
  ++ = Incremento
  -- = Descremento
--------------------------------------------------------
*/

// Exemplo de incremento
let idade = 18;

// Utilizando o operador na frente da variavel ele realiza o incremento depois de a variavel ser exibida
console.log("*** Incremento na frente ****")
console.log(idade++);
console.log(idade);
// Utilizando o operador atrás da variavel ele realiza o incremento antes de a variavel ser exibida
console.log("*** Incremento atras ****")
console.log(++idade);
console.log(idade);

// Exemplo de descremento

// Utilizando o operador na frente da variavel ele realiza o decremento depois de a variavel ser exibida
console.log("*** decremento na frente ****")
console.log(idade--);
console.log(idade);
// Utilizando o operador atrás da variavel ele realiza o decremento antes de a variavel ser exibida
console.log("*** decremento atras ****")
console.log(--idade);
console.log(idade);

