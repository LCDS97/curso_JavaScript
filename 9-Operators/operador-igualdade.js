// Operador de Igualdades
// Igualdade estrita
// Além de comparar os valores, ele também compara os tipos

console.log ( 1  === 1);

console.log ('1' === 1);

// Igualdade solta
// Aqui ele ignora o tipo e só compara os valores
// Caso seja tipos diferentes ele converte o tipo para dar true
console.log (1 == 1);
console.log ('1' == 1);

